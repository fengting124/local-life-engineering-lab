-- =======================================================
-- V6：为 shop 表新增 price（门店价格）字段
--
-- 背景：
--   下单模块（V5）需要从 shop 表读取原价，计算 order_info.original_amount。
--   V2 建表时未加 price 字段，用增量迁移补充，保持迁移历史完整。
--
-- 为什么 price 存门店表而不是商品表
--   当前 LocalLife 是「门店级」交易（用户购买门店的服务/套餐），
--   一个门店只有一个价格（类似「代金券面值」），没有独立的 SKU/商品表。
--   后续若升级为商品级交易，可以再建 product 表，当前阶段简化处理。
--
-- 金额单位：
--   price 以「分」（INT UNSIGNED）存储，避免浮点数精度问题。
--   示例：99 元 → price = 9900。
-- =======================================================

ALTER TABLE `shop`
    ADD COLUMN `price` INT UNSIGNED NOT NULL DEFAULT 0
        COMMENT '门店价格（分），如 9900 = 99元，0 表示价格待定' AFTER `score`;
